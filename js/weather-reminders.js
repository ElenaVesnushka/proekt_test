  function hashString(s) {
    var h = 0;
    for (var i = 0; i < s.length; i += 1) {
      h = (h << 5) - h + s.charCodeAt(i);
      h |= 0;
    }
    return Math.abs(h);
  }

  /**
   * Нормализованный снимок для категорий:
   * { tempMax: number, weatherCode: number, windKmh: number, precipMm: number }
   */
  function mapWeatherToCategory(w) {
    var code = Number(w.weatherCode);
    if (!Number.isFinite(code)) {
      code = -1;
    }
    var t = Number(w.tempMax);
    if (!Number.isFinite(t)) {
      t = 15;
    }
    var wind = w.windKmh;
    var p = w.precipMm || 0;

    if ((code >= 71 && code <= 77) || code === 85 || code === 86) return "snow";
    if (code === 95 || code === 96 || code === 99 || code === 65 || code === 80 || code === 81 || code === 82) {
      return "rain";
    }
    if (p >= 8) return "rain";
    if (wind >= 40) return "windy";
    if (t > 28) return "hot";
    if (code === 61 || code === 63 || code === 82 || p > 0.4) return "rain";
    if (code === 45 || code === 48) return "fog";
    if (t < 10) return "cold";
    if (code === 3) return "cloudy";
    if ((code === 0 || code === 1 || code === 2) && t >= 20 && t <= 28) return "sunny";
    if (t >= 10 && t <= 19) return "cool";
    if (code === 0 || code === 1 || code === 2) return "sunny";
    return "neutral";
  }

  function getWeatherReminder(weatherSnapshot, dateIso) {
    var cat = mapWeatherToCategory(weatherSnapshot);
    var list = REMINDERS[cat] || REMINDERS.neutral;
    var idx = hashString(dateIso + "|" + cat + "|rem") % list.length;
    return list[idx];
  }

  /**
   * Текущая погода (Open-Meteo v1 forecast current).
   */
  function getWeatherByCoords(lat, lon, timezone) {
    var params = new URLSearchParams({
      latitude: String(lat),
      longitude: String(lon),
      timezone: timezone || "auto",
      current: "temperature_2m,weather_code,wind_speed_10m,precipitation"
    });
    return fetch("https://api.open-meteo.com/v1/forecast?" + params.toString()).then(function (res) {
      if (!res.ok) throw new Error("current weather");
      return res.json();
    });
  }

  function snapshotFromCurrent(cur) {
    if (!cur) return null;
    return {
      tempMax: Math.round(cur.temperature_2m),
      weatherCode: cur.weather_code,
      windKmh: Math.round(cur.wind_speed_10m),
      precipMm: cur.precipitation != null ? cur.precipitation : 0
    };
  }

  function snapshotFromDailyRow(daily, idx) {
    idx = idx || 0;
    if (!daily || !daily.time || !daily.time[idx]) return null;
    var tmax = daily.temperature_2m_max[idx];
    var tmin = daily.temperature_2m_min[idx];
    return {
      tempMax: Math.round((tmax + tmin) / 2),
      weatherCode: daily.weather_code[idx],
      windKmh: Math.round(daily.windspeed_10m_max[idx] || 0),
      precipMm: Number(daily.precipitation_sum[idx]) || 0
    };
  }

